// -------------------------------------------- Imports -------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

import { SUBMIT_REQUEST } from './types';

// ------------------------------------------------------------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

const initialState = {};

// ------------------------------------------------------------------------------------------------------- //

const weatherData = (state = initialState, action) => {
    switch (action.type) {
        case SUBMIT_REQUEST:
            return {
                ...state,
                initialState: action.payload
            }
    
        default:
            return state;
    }
}

// ------------------------------------------------------------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

export default weatherData;

