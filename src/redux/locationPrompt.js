// -------------------------------------------- Imports -------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

import { LOCATION_REQUEST } from './types';

// ------------------------------------------------------------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

const locationPrompt = (state = {location: false}, action) => {
    switch (action.type) {
        case LOCATION_REQUEST:
            return {
                location: true
            }
        default:
            return state;
    }
}

// ------------------------------------------------------------------------------------------------------- //
// ------------------------------------------------------------------------------------------------------- //

export default locationPrompt;

